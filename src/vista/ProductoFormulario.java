//	}
//	if(((txtNombre.getText() == tbProducto.getValueAt(fila, 1)) && ((txtTemperatura.getText()) == tbProducto.getValueAt(fila, 2).toString()) && (txtValorBase.getText() == tbProducto.getValueAt(fila, 3).toString()))){
