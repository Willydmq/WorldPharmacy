    public void mostrarTabla() {
	String datos[] = new String[4];
	DefaultTableModel Tproducto = new DefaultTableModel();
	Tproducto = (DefaultTableModel) tbProducto.getModel();
	Tproducto.getDataVector().clear();
//        for(int i =0; i<Teditorial.getRowCount();i++){
//            Teditorial.removeRow(i);
//        }
	ArrayList<Producto> listaProducto = new ArrayList();
	listaProducto = servicio.listarProducto();
	for (Producto x : listaProducto) {
	    datos[0] = x.getId_pro();
	    datos[1] = x.getNombre_pro();
	    datos[2] = String.valueOf(x.getTemperatura_pro());
	    datos[3] = String.valueOf(x.getValorBase_pro());
	    Tproducto.addRow(datos);
	}
	Tproducto = (DefaultTableModel) tbProducto.getModel();
    }

